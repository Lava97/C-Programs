int ABS(int number){
  return number<0 ? -number : number;
}

long int LABS(long number){
  return number<0 ? -number : number;
}

long long int LLABS(long long number){
  return number<0 ? -number : number;
}

float FABSF(float number){
  return number<0 ? -number : number;
}

double FABS(double number){
  return number<0 ? -number : number;
}

long double FABSL(long double number){
  return number<0 ? -number : number;
}
